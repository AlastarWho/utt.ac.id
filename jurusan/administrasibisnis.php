                    <h2 class="section-title text-center">Administrasi Bisnis</h2>
<p>Universitas Tawa-Tawa dengan bangga mempersembahkan Program Studi Administrasi Bisnis, program unggulan yang dirancang untuk mempersiapkan mahasiswa menjadi pemimpin bisnis yang inovatif dan berdaya saing tinggi. Mari kita jelajahi lebih dalam tentang program studi ini dan mengapa Anda harus memilihnya!</p>
<h3>Mengapa Memilih Administrasi Bisnis?</h3>
<ul>
    <li><strong>Kurikulum Terdepan:</strong> Program Studi Administrasi Bisnis di Universitas Tawa-Tawa menawarkan kurikulum yang terus diperbarui sesuai dengan tren bisnis global. Mata kuliah mencakup berbagai topik penting seperti manajemen keuangan, pemasaran, sumber daya manusia, kewirausahaan, dan strategi bisnis.</li>
    <li><strong>Dosen Berpengalaman:</strong> Belajar langsung dari para ahli di bidangnya. Dosen-dosen kami memiliki pengalaman industri dan akademis yang luas, siap membimbing Anda melalui teori dan praktik yang diperlukan untuk sukses di dunia bisnis.</li>
    <li><strong>Fasilitas Modern:</strong> Nikmati akses ke fasilitas belajar yang dilengkapi dengan teknologi terkini. Kampus kami juga memiliki pusat penelitian bisnis, ruang simulasi pasar, dan perpustakaan yang lengkap untuk mendukung proses belajar Anda.</li>
    <li><strong>Peluang Karir yang Luas:</strong> Lulusan Administrasi Bisnis memiliki peluang karir yang luas di berbagai industri. Anda dapat bekerja sebagai manajer bisnis, konsultan manajemen, analis keuangan, spesialis pemasaran, dan masih banyak lagi. Koneksi industri yang kuat dan program magang kami akan membantu Anda memulai karir dengan langkah yang kokoh.</li>
    <li><strong>Komunitas dan Kegiatan Mahasiswa:</strong> Bergabunglah dengan komunitas mahasiswa yang aktif dan beragam. Ikuti berbagai kegiatan ekstrakurikuler seperti klub bisnis, kompetisi kewirausahaan, dan seminar industri. Pengalaman ini tidak hanya memperkaya pengetahuan Anda tetapi juga memperluas jaringan profesional Anda.</li>
</ul>
<br>
<h3>Kegiatan Akademik dan Non-Akademik</h3>
<ul>
    <li><strong>Proyek Akhir dan Magang:</strong> Mahasiswa Administrasi Bisnis akan terlibat dalam proyek akhir yang menantang dan relevan dengan kebutuhan industri. Program magang kami memungkinkan Anda untuk mendapatkan pengalaman langsung di perusahaan terkemuka.</li>
    <li><strong>Seminar dan Workshop:</strong> Kami secara rutin mengadakan seminar dan workshop yang menghadirkan pembicara dari kalangan profesional industri, memberikan wawasan berharga tentang tren terbaru dan peluang di bidang bisnis.</li>
    <li><strong>Kompetisi dan Penghargaan:</strong> Berpartisipasi dalam berbagai kompetisi bisnis baik di tingkat nasional maupun internasional. Raih penghargaan dan tunjukkan kemampuan Anda kepada dunia.</li>
</ul>
