                    <h2 class="section-title text-center">Sistem Informasi</h2>
<p>Universitas Tawa-Tawa dengan bangga mempersembahkan Program Studi Sistem Informasi, program unggulan yang dirancang untuk mempersiapkan mahasiswa menjadi ahli dalam manajemen informasi dan teknologi yang inovatif serta berdaya saing tinggi. Mari kita jelajahi lebih dalam tentang program studi ini dan mengapa Anda harus memilihnya!</p>
<h3>Mengapa Memilih Sistem Informasi?</h3>
<ul>
    <li><strong>Kurikulum Terdepan:</strong> Program Studi Sistem Informasi di Universitas Tawa-Tawa menawarkan kurikulum yang terus diperbarui sesuai dengan perkembangan teknologi terkini. Mata kuliah mencakup berbagai topik penting seperti analisis dan desain sistem, manajemen basis data, e-commerce, sistem informasi geografis, dan manajemen proyek TI.</li>
    <li><strong>Dosen Berpengalaman:</strong> Belajar langsung dari para ahli di bidangnya. Dosen-dosen kami memiliki pengalaman industri dan akademis yang luas, siap membimbing Anda melalui teori dan praktik yang diperlukan untuk sukses di dunia teknologi dan manajemen informasi.</li>
    <li><strong>Fasilitas Modern:</strong> Nikmati akses ke laboratorium komputer yang dilengkapi dengan perangkat keras dan perangkat lunak terbaru. Kampus kami juga memiliki fasilitas pendukung seperti perpustakaan digital, ruang inovasi, dan pusat penelitian yang mendorong kreativitas dan pengembangan keterampilan teknis.</li>
    <li><strong>Peluang Karir yang Luas:</strong> Lulusan Sistem Informasi memiliki peluang karir yang luas di berbagai industri. Anda dapat bekerja sebagai analis sistem, manajer proyek TI, spesialis basis data, konsultan IT, dan masih banyak lagi. Koneksi industri yang kuat dan program magang kami akan membantu Anda memulai karir dengan langkah yang kokoh.</li>
    <li><strong>Komunitas dan Kegiatan Mahasiswa:</strong> Bergabunglah dengan komunitas mahasiswa yang aktif dan beragam. Ikuti berbagai kegiatan ekstrakurikuler seperti klub TI, hackathon, dan seminar teknologi. Pengalaman ini tidak hanya memperkaya pengetahuan Anda tetapi juga memperluas jaringan profesional Anda.</li>
</ul>
<br>
<h3>Kegiatan Akademik dan Non-Akademik</h3>
<ul>
    <li><strong>Proyek Akhir dan Magang:</strong> Mahasiswa Sistem Informasi akan terlibat dalam proyek akhir yang menantang dan relevan dengan kebutuhan industri. Program magang kami memungkinkan Anda untuk mendapatkan pengalaman langsung di perusahaan teknologi terkemuka.</li>
    <li><strong>Seminar dan Workshop:</strong> Kami secara rutin mengadakan seminar dan workshop yang menghadirkan pembicara dari kalangan profesional industri, memberikan wawasan berharga tentang tren terbaru dan peluang di bidang sistem informasi.</li>
    <li><strong>Kompetisi dan Penghargaan:</strong> Berpartisipasi dalam berbagai kompetisi teknologi baik di tingkat nasional maupun internasional. Raih penghargaan dan tunjukkan kemampuan Anda kepada dunia.</li>
</ul>
