                    <h2 class="section-title text-center">Fisika</h2>
<p>Universitas Tawa-Tawa dengan bangga mempersembahkan Program Studi Fisika, program unggulan yang dirancang untuk mempersiapkan mahasiswa menjadi ilmuwan dan peneliti yang inovatif dalam memahami fenomena alam dan teknologi. Mari kita jelajahi lebih dalam tentang program studi ini dan mengapa Anda harus memilihnya!</p>
<h3>Mengapa Memilih Fisika?</h3>
<ul>
    <li><strong>Kurikulum Terdepan:</strong> Program Studi Fisika di Universitas Tawa-Tawa menawarkan kurikulum yang komprehensif dan terus diperbarui sesuai dengan perkembangan terbaru di bidang fisika. Mata kuliah mencakup berbagai topik penting seperti mekanika, elektromagnetisme, termodinamika, fisika kuantum, dan fisika material.</li>
    <li><strong>Dosen Berpengalaman:</strong> Belajar langsung dari para ahli di bidangnya. Dosen-dosen kami memiliki pengalaman akademis dan penelitian yang luas, siap membimbing Anda melalui teori dan praktik yang diperlukan untuk sukses di dunia fisika.</li>
    <li><strong>Fasilitas Modern:</strong> Nikmati akses ke laboratorium fisika yang dilengkapi dengan peralatan canggih. Kampus kami juga memiliki pusat penelitian, observatorium, dan fasilitas komputasi yang mendukung eksperimen dan penelitian Anda.</li>
    <li><strong>Peluang Karir yang Luas:</strong> Lulusan Fisika memiliki peluang karir yang luas di berbagai bidang. Anda dapat bekerja sebagai peneliti, dosen, analis data, insinyur fisika, dan masih banyak lagi. Koneksi industri yang kuat dan program magang kami akan membantu Anda memulai karir dengan langkah yang kokoh.</li>
    <li><strong>Komunitas dan Kegiatan Mahasiswa:</strong> Bergabunglah dengan komunitas mahasiswa yang aktif dan beragam. Ikuti berbagai kegiatan ekstrakurikuler seperti klub fisika, kompetisi sains, dan seminar ilmiah. Pengalaman ini tidak hanya memperkaya pengetahuan Anda tetapi juga memperluas jaringan profesional Anda.</li>
</ul>
<br>
<h3>Kegiatan Akademik dan Non-Akademik</h3>
<ul>
    <li><strong>Proyek Akhir dan Magang:</strong> Mahasiswa Fisika akan terlibat dalam proyek akhir yang menantang dan relevan dengan kebutuhan industri dan penelitian. Program magang kami memungkinkan Anda untuk mendapatkan pengalaman langsung di lembaga penelitian atau perusahaan teknologi terkemuka.</li>
    <li><strong>Seminar dan Workshop:</strong> Kami secara rutin mengadakan seminar dan workshop yang menghadirkan pembicara dari kalangan profesional dan akademisi terkemuka, memberikan wawasan berharga tentang tren terbaru dan peluang di bidang fisika.</li>
    <li><strong>Kompetisi dan Penghargaan:</strong> Berpartisipasi dalam berbagai kompetisi sains baik di tingkat nasional maupun internasional. Raih penghargaan dan tunjukkan kemampuan Anda kepada dunia.</li>
</ul>
